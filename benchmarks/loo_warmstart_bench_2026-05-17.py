#!/usr/bin/env python3
"""
Smoke benchmark: warm-start / distilled LOO teachers vs full retrain.

Generates synthetic data sized to match the picker scale described in
`_picker_lib._make_teacher`'s docstring (~4000 rows × 80 features ×
12-36 cells). Runs three LOO strategies on a held-out validation
split and reports wall-time and argmin accuracy:

  - retrain   : the legacy baseline; re-fit every cell from scratch
                with one feature dropped.
  - distilled : `fit_distilled_teacher` per cell (soft labels from
                the full teacher; HISTGB_FAST default).
  - residual  : `fit_residual_teacher` per cell (correction on the
                residual w.r.t. the full teacher's prediction on a
                mean-imputed full-schema matrix).

Outputs a TSV with per-feature timings + a markdown summary written to
`benchmarks/loo_warmstart_bench_2026-05-17.md`.

Synthetic-data design:
- 1500 "images", 4 size classes -> ~4500 rows after stacking.
- 30 latent features driving the target; 50 noise features.
- 12 cells (per the zenjpeg hybrid-heads scale) generated by 12
  distinct random tree-like transforms of the latent features +
  per-cell noise; this gives realistic argmin selection patterns
  (different cells "win" on different inputs).
- ~10 LOO features sampled from the latent + noise pool so we cover
  both "drop something important" and "drop something irrelevant".

This is a SMOKE test on synthetic data, not a real-codec benchmark.
The goal is to validate the new helpers' wall-time vs accuracy
tradeoff before wiring them through `feature_ablation.py` on real
codec data.
"""

from __future__ import annotations

import sys
import time
from collections import defaultdict
from pathlib import Path

import numpy as np

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "zentrain" / "tools"))

import _picker_lib as picker  # noqa: E402


# ---------- Synthetic data ----------


def make_synthetic_dataset(
    n_rows: int = 4500,
    n_latent: int = 30,
    n_noise: int = 50,
    n_cells: int = 12,
    seed: int = 0xC0FFEE,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, list[str]]:
    """Return (X, Y, reach, feat_names).

    X: (n_rows, n_latent + n_noise) — latent feats first, noise after.
    Y: (n_rows, n_cells) log-bytes-like target.
    reach: (n_rows, n_cells) bool — reachability mask (mostly True).
    feat_names: ["feat_lat_0", ..., "feat_noise_0", ...]
    """
    rng = np.random.default_rng(seed)
    n_total = n_latent + n_noise

    # Correlated features: latent has signal, noise is iid Gaussian.
    X_lat = rng.standard_normal((n_rows, n_latent)).astype(np.float32)
    X_noise = rng.standard_normal((n_rows, n_noise)).astype(np.float32)
    X = np.concatenate([X_lat, X_noise], axis=1)

    # Per-cell target: a tree-flavoured nonlinear combination of
    # random subsets of the latent features. Same X drives all cells,
    # but each cell weights different latent features and adds its
    # own residual noise so the argmin selection varies row by row.
    Y = np.zeros((n_rows, n_cells), dtype=np.float32)
    for c in range(n_cells):
        # Random subset of latent feats this cell "cares about".
        k_use = rng.integers(6, 14)
        idx_use = rng.choice(n_latent, size=int(k_use), replace=False)
        weights = rng.standard_normal(int(k_use)).astype(np.float32)
        z = X_lat[:, idx_use] @ weights
        # Nonlinearities: clipped quadratic and a sign-aware sqrt.
        nz = (
            0.6 * z
            + 0.25 * np.tanh(z) * np.abs(z)
            + 0.1 * np.sign(z) * np.sqrt(np.abs(z) + 1e-3)
        )
        cell_offset = rng.standard_normal() * 0.3
        Y[:, c] = nz + cell_offset + rng.standard_normal(n_rows).astype(np.float32) * 0.08

    # Reachability: pretend ~80% of cells are reachable at each row.
    reach = rng.random((n_rows, n_cells)) < 0.8
    # Guarantee every row has at least one reachable cell.
    no_reach = ~reach.any(axis=1)
    reach[no_reach, 0] = True

    feat_names = [f"feat_lat_{i}" for i in range(n_latent)] + [
        f"feat_noise_{i}" for i in range(n_noise)
    ]
    return X, Y, reach, feat_names


# ---------- Evaluation ----------


def evaluate_argmin_simple(Y_pred: np.ndarray, Y_actual: np.ndarray) -> dict:
    """Stripped-down argmin metrics on dense (non-NaN) Y. Synthetic
    data has no NaN so this is sufficient.
    """
    # Use exp() consistency with picker's evaluate_argmin (log-bytes
    # → bytes), even though synthetic Y isn't log-domain — the rank
    # is preserved either way and exp() with clipping protects against
    # extreme predictions.
    ab = np.exp(np.clip(Y_actual, -30, 30))
    pb = np.exp(np.clip(Y_pred, -30, 30))
    actual_best = np.argmin(ab, axis=1)
    pred_best = np.argmin(pb, axis=1)
    correct = (actual_best == pred_best).sum()
    n = Y_actual.shape[0]
    # Per-row overhead vs best.
    row_idx = np.arange(n)
    ab_at_pred = ab[row_idx, pred_best]
    ab_at_actual = ab[row_idx, actual_best]
    overheads = (ab_at_pred - ab_at_actual) / np.maximum(ab_at_actual, 1e-30)
    return {
        "n": int(n),
        "argmin_acc": float(correct / n),
        "mean_overhead_pct": float(100 * overheads.mean()),
        "p50_overhead_pct": float(100 * np.percentile(overheads, 50)),
        "p90_overhead_pct": float(100 * np.percentile(overheads, 90)),
    }


# ---------- LOO strategies ----------


def loo_drop_col(X: np.ndarray, col: int, fill_with_mean: bool = True) -> np.ndarray:
    """Return X with `col` replaced by the column mean (mean-imputed)
    if `fill_with_mean`, or actually dropped (n_feats - 1)."""
    if fill_with_mean:
        out = X.copy()
        out[:, col] = X[:, col].mean()
        return out
    return np.delete(X, col, axis=1)


def run_retrain_loo(
    X_tr: np.ndarray,
    Y_tr: np.ndarray,
    reach_tr: np.ndarray,
    X_va: np.ndarray,
    Y_va: np.ndarray,
    drop_col: int,
    params: dict,
) -> tuple[dict, float]:
    """Baseline: re-fit every cell from scratch on the LOO matrix."""
    X_tr_loo = loo_drop_col(X_tr, drop_col, fill_with_mean=False)
    X_va_loo = loo_drop_col(X_va, drop_col, fill_with_mean=False)
    t0 = time.monotonic()
    teachers = picker.train_teachers_per_cell_parallel(
        X_tr_loo, Y_tr, reach_tr, params=params, n_jobs=-1, label="retrain"
    )
    elapsed = time.monotonic() - t0
    means = np.array(
        [np.nanmean(Y_tr[:, c]) if reach_tr[:, c].any() else 0.0 for c in range(Y_tr.shape[1])],
        dtype=np.float32,
    )
    Y_pred = picker.teacher_predict_all(teachers, X_va_loo, means)
    return evaluate_argmin_simple(Y_pred, Y_va), elapsed


def run_distilled_loo(
    full_teachers: list,
    X_tr: np.ndarray,
    Y_tr: np.ndarray,
    reach_tr: np.ndarray,
    X_va: np.ndarray,
    Y_va: np.ndarray,
    drop_col: int,
    params: dict,
) -> tuple[dict, float]:
    """Distilled: soft labels from the full teacher, fit on X_loo."""
    X_tr_loo = loo_drop_col(X_tr, drop_col, fill_with_mean=False)
    X_va_loo = loo_drop_col(X_va, drop_col, fill_with_mean=False)
    t0 = time.monotonic()
    distilled = picker.train_loo_teachers_distilled_parallel(
        full_teachers,
        X_full_tr=X_tr,
        X_loo_tr=X_tr_loo,
        reach_tr=reach_tr,
        Y_tr=Y_tr,
        params=params,
        n_jobs=-1,
        label="distilled",
    )
    elapsed = time.monotonic() - t0
    means = np.array(
        [np.nanmean(Y_tr[:, c]) if reach_tr[:, c].any() else 0.0 for c in range(Y_tr.shape[1])],
        dtype=np.float32,
    )
    Y_pred = picker.teacher_predict_all(distilled, X_va_loo, means)
    return evaluate_argmin_simple(Y_pred, Y_va), elapsed


def run_residual_loo(
    full_teachers: list,
    X_tr: np.ndarray,
    Y_tr: np.ndarray,
    reach_tr: np.ndarray,
    X_va: np.ndarray,
    Y_va: np.ndarray,
    drop_col: int,
    params: dict,
) -> tuple[dict, float]:
    """Residual: correction GBM on y - full_teacher.predict(X_imputed)."""
    X_tr_imputed = loo_drop_col(X_tr, drop_col, fill_with_mean=True)
    X_va_imputed = loo_drop_col(X_va, drop_col, fill_with_mean=True)
    X_tr_loo = loo_drop_col(X_tr, drop_col, fill_with_mean=False)
    X_va_loo = loo_drop_col(X_va, drop_col, fill_with_mean=False)

    t0 = time.monotonic()
    n_cells = Y_tr.shape[1]
    wrappers = [None] * n_cells
    for c in range(n_cells):
        full = full_teachers[c]
        if full is None:
            continue
        mask = reach_tr[:, c] & ~np.isnan(Y_tr[:, c])
        if mask.sum() < 50:
            continue
        wrap = picker.fit_residual_teacher(
            full,
            X_full_imputed=X_tr_imputed,
            X_loo=X_tr_loo,
            y=Y_tr[:, c],
            params=params,
            sample_mask=mask,
        )
        # Wire the prior_predict closure for the val side. The
        # imputed val matrix is precomputed once here.
        captured_full = full
        captured_imputed_va = X_va_imputed

        def _prior_for_val(_x_loo: np.ndarray, _full=captured_full, _imp=captured_imputed_va) -> np.ndarray:
            return _full.predict(_imp)

        wrap.prior_predict = _prior_for_val
        wrappers[c] = wrap
    elapsed = time.monotonic() - t0

    means = np.array(
        [np.nanmean(Y_tr[:, c]) if reach_tr[:, c].any() else 0.0 for c in range(n_cells)],
        dtype=np.float32,
    )
    Y_pred = np.zeros_like(Y_va)
    for c in range(n_cells):
        w = wrappers[c]
        if w is None:
            Y_pred[:, c] = means[c]
        else:
            Y_pred[:, c] = w.predict(X_va_loo)
    return evaluate_argmin_simple(Y_pred, Y_va), elapsed


# ---------- Main ----------


def main() -> int:
    BENCH_DIR = Path(__file__).resolve().parent
    OUT_TSV = BENCH_DIR / "loo_warmstart_bench_2026-05-17.tsv"
    OUT_MD = BENCH_DIR / "loo_warmstart_bench_2026-05-17.md"

    sys.stderr.write("[bench] generating synthetic dataset...\n")
    X, Y, reach, feat_names = make_synthetic_dataset()
    sys.stderr.write(
        f"[bench] X={X.shape} Y={Y.shape} reach.mean={reach.mean():.3f}\n"
    )

    # Image-like holdout: 20% rows.
    rng = np.random.default_rng(0xCAFE)
    n = X.shape[0]
    perm = rng.permutation(n)
    n_val = int(0.2 * n)
    val_idx = perm[:n_val]
    train_idx = perm[n_val:]
    X_tr, Y_tr, reach_tr = X[train_idx], Y[train_idx], reach[train_idx]
    X_va, Y_va = X[val_idx], Y[val_idx]
    sys.stderr.write(f"[bench] train={len(train_idx)} val={len(val_idx)}\n")

    # ---- Pre-train the full-schema teachers once (shared input to
    # the distilled + residual variants).
    sys.stderr.write("\n[bench] training full-schema teachers (HISTGB_FULL)...\n")
    t0 = time.monotonic()
    full_teachers = picker.train_teachers_per_cell_parallel(
        X_tr, Y_tr, reach_tr, params=picker.HISTGB_FULL, n_jobs=-1, label="full"
    )
    full_train_time = time.monotonic() - t0
    sys.stderr.write(f"[bench] full teachers fit in {full_train_time:.2f}s\n")

    means = np.array(
        [np.nanmean(Y_tr[:, c]) if reach_tr[:, c].any() else 0.0 for c in range(Y_tr.shape[1])],
        dtype=np.float32,
    )
    Y_pred_baseline = picker.teacher_predict_all(full_teachers, X_va, means)
    baseline_metrics = evaluate_argmin_simple(Y_pred_baseline, Y_va)
    sys.stderr.write(
        f"[bench] full baseline: argmin_acc={baseline_metrics['argmin_acc']:.4f} "
        f"mean_overhead={baseline_metrics['mean_overhead_pct']:.2f}%\n"
    )

    # ---- Pick 10 LOO features: 6 latent (strong signal) + 4 noise.
    rng2 = np.random.default_rng(0xBEEF)
    latent_cols = rng2.choice(30, size=6, replace=False).tolist()
    noise_cols = (30 + rng2.choice(50, size=4, replace=False)).tolist()
    loo_cols = sorted(latent_cols + noise_cols)
    sys.stderr.write(f"\n[bench] LOO features: {[feat_names[c] for c in loo_cols]}\n")

    # ---- For each LOO feature: retrain at FULL+FAST presets vs
    # distilled (HISTGB_FAST and a medium variant) vs residual.
    # feature_ablation.py uses HISTGB_FAST-ish params (max_iter=100,
    # max_depth=4) for the ablation hot path; that's the realistic
    # "what could this replace" baseline. We measure both presets
    # separately so the speedup vs ablation-baseline is honest.
    HISTGB_MEDIUM = dict(picker.HISTGB_FAST)
    HISTGB_MEDIUM["max_iter"] = 200
    HISTGB_MEDIUM["max_depth"] = 6

    rows = []
    for col in loo_cols:
        fname = feat_names[col]
        sys.stderr.write(f"\n[bench] --- LOO drop col {col} ({fname}) ---\n")

        m_re_full, t_re_full = run_retrain_loo(
            X_tr, Y_tr, reach_tr, X_va, Y_va, col, params=picker.HISTGB_FULL
        )
        sys.stderr.write(
            f"  retrain(FULL)  : {t_re_full:.2f}s argmin_acc={m_re_full['argmin_acc']:.4f} "
            f"mean_ov={m_re_full['mean_overhead_pct']:.2f}%\n"
        )

        m_re_fast, t_re_fast = run_retrain_loo(
            X_tr, Y_tr, reach_tr, X_va, Y_va, col, params=picker.HISTGB_FAST
        )
        sys.stderr.write(
            f"  retrain(FAST)  : {t_re_fast:.2f}s argmin_acc={m_re_fast['argmin_acc']:.4f} "
            f"mean_ov={m_re_fast['mean_overhead_pct']:.2f}%\n"
        )

        m_di_fast, t_di_fast = run_distilled_loo(
            full_teachers, X_tr, Y_tr, reach_tr, X_va, Y_va, col,
            params=picker.HISTGB_FAST,
        )
        sys.stderr.write(
            f"  distill(FAST)  : {t_di_fast:.2f}s argmin_acc={m_di_fast['argmin_acc']:.4f} "
            f"mean_ov={m_di_fast['mean_overhead_pct']:.2f}%\n"
        )

        m_di_med, t_di_med = run_distilled_loo(
            full_teachers, X_tr, Y_tr, reach_tr, X_va, Y_va, col,
            params=HISTGB_MEDIUM,
        )
        sys.stderr.write(
            f"  distill(MED)   : {t_di_med:.2f}s argmin_acc={m_di_med['argmin_acc']:.4f} "
            f"mean_ov={m_di_med['mean_overhead_pct']:.2f}%\n"
        )

        m_rs, t_rs = run_residual_loo(
            full_teachers, X_tr, Y_tr, reach_tr, X_va, Y_va, col,
            params=picker.HISTGB_FAST,
        )
        sys.stderr.write(
            f"  residual(FAST) : {t_rs:.2f}s argmin_acc={m_rs['argmin_acc']:.4f} "
            f"mean_ov={m_rs['mean_overhead_pct']:.2f}%\n"
        )

        rows.append(
            {
                "feature": fname,
                "col": col,
                "retrain_full_time_s": t_re_full,
                "retrain_full_argmin_acc": m_re_full["argmin_acc"],
                "retrain_full_mean_ov_pct": m_re_full["mean_overhead_pct"],
                "retrain_fast_time_s": t_re_fast,
                "retrain_fast_argmin_acc": m_re_fast["argmin_acc"],
                "retrain_fast_mean_ov_pct": m_re_fast["mean_overhead_pct"],
                "distilled_fast_time_s": t_di_fast,
                "distilled_fast_argmin_acc": m_di_fast["argmin_acc"],
                "distilled_fast_mean_ov_pct": m_di_fast["mean_overhead_pct"],
                "distilled_med_time_s": t_di_med,
                "distilled_med_argmin_acc": m_di_med["argmin_acc"],
                "distilled_med_mean_ov_pct": m_di_med["mean_overhead_pct"],
                "residual_time_s": t_rs,
                "residual_argmin_acc": m_rs["argmin_acc"],
                "residual_mean_ov_pct": m_rs["mean_overhead_pct"],
            }
        )

    # ---- Persist TSV.
    cols = list(rows[0].keys())
    with open(OUT_TSV, "w") as f:
        f.write("\t".join(cols) + "\n")
        for r in rows:
            f.write("\t".join(str(r[c]) for c in cols) + "\n")
    sys.stderr.write(f"\n[bench] wrote {OUT_TSV}\n")

    # ---- Aggregate stats.
    def agg(key: str) -> tuple[float, float]:
        vals = [r[key] for r in rows]
        return float(np.mean(vals)), float(np.std(vals))

    re_full_t_mean, re_full_t_std = agg("retrain_full_time_s")
    re_full_a_mean, _ = agg("retrain_full_argmin_acc")
    re_fast_t_mean, re_fast_t_std = agg("retrain_fast_time_s")
    re_fast_a_mean, _ = agg("retrain_fast_argmin_acc")
    di_fast_t_mean, di_fast_t_std = agg("distilled_fast_time_s")
    di_fast_a_mean, _ = agg("distilled_fast_argmin_acc")
    di_med_t_mean, di_med_t_std = agg("distilled_med_time_s")
    di_med_a_mean, _ = agg("distilled_med_argmin_acc")
    rs_t_mean, rs_t_std = agg("residual_time_s")
    rs_a_mean, _ = agg("residual_argmin_acc")

    def speed_acc(other_t: float, other_a: float, base_t: float, base_a: float):
        speedup = base_t / other_t if other_t > 0 else float("inf")
        loss_pp = 100 * (base_a - other_a)
        return speedup, loss_pp

    # Two comparisons: vs FULL retrain, vs FAST retrain. The latter
    # matches feature_ablation.py's actual default and is the honest
    # speedup measurement for the LOO ablation hot path.
    di_fast_su_full, di_fast_loss_full = speed_acc(
        di_fast_t_mean, di_fast_a_mean, re_full_t_mean, re_full_a_mean
    )
    di_fast_su_fast, di_fast_loss_fast = speed_acc(
        di_fast_t_mean, di_fast_a_mean, re_fast_t_mean, re_fast_a_mean
    )
    di_med_su_full, di_med_loss_full = speed_acc(
        di_med_t_mean, di_med_a_mean, re_full_t_mean, re_full_a_mean
    )
    di_med_su_fast, di_med_loss_fast = speed_acc(
        di_med_t_mean, di_med_a_mean, re_fast_t_mean, re_fast_a_mean
    )
    rs_su_full, rs_loss_full = speed_acc(
        rs_t_mean, rs_a_mean, re_full_t_mean, re_full_a_mean
    )
    rs_su_fast, rs_loss_fast = speed_acc(
        rs_t_mean, rs_a_mean, re_fast_t_mean, re_fast_a_mean
    )

    sys.stderr.write("\n=========================== summary ===========================\n")
    sys.stderr.write(
        f"  retrain(FULL)  : wall {re_full_t_mean:.2f}s ± {re_full_t_std:.2f}, acc {re_full_a_mean:.4f}\n"
    )
    sys.stderr.write(
        f"  retrain(FAST)  : wall {re_fast_t_mean:.2f}s ± {re_fast_t_std:.2f}, acc {re_fast_a_mean:.4f}\n"
    )
    sys.stderr.write(
        f"  distill(FAST)  : wall {di_fast_t_mean:.2f}s ± {di_fast_t_std:.2f}, acc {di_fast_a_mean:.4f} "
        f"(vs FULL: {di_fast_su_full:.2f}× / {di_fast_loss_full:+.2f}pp; "
        f"vs FAST: {di_fast_su_fast:.2f}× / {di_fast_loss_fast:+.2f}pp)\n"
    )
    sys.stderr.write(
        f"  distill(MED)   : wall {di_med_t_mean:.2f}s ± {di_med_t_std:.2f}, acc {di_med_a_mean:.4f} "
        f"(vs FULL: {di_med_su_full:.2f}× / {di_med_loss_full:+.2f}pp; "
        f"vs FAST: {di_med_su_fast:.2f}× / {di_med_loss_fast:+.2f}pp)\n"
    )
    sys.stderr.write(
        f"  residual(FAST) : wall {rs_t_mean:.2f}s ± {rs_t_std:.2f}, acc {rs_a_mean:.4f} "
        f"(vs FULL: {rs_su_full:.2f}× / {rs_loss_full:+.2f}pp; "
        f"vs FAST: {rs_su_fast:.2f}× / {rs_loss_fast:+.2f}pp)\n"
    )

    # ---- Write markdown.
    lines = [
        "# LOO warm-start benchmark — 2026-05-17",
        "",
        "Synthetic-data smoke test for `_picker_lib`'s new",
        "`fit_distilled_teacher` / `fit_residual_teacher` /",
        "`train_loo_teachers_distilled_parallel` helpers.",
        "",
        "## Setup",
        "",
        f"- Rows: {X.shape[0]} (train {len(train_idx)} / val {len(val_idx)})",
        f"- Features: {X.shape[1]} ({30} latent + {50} noise)",
        f"- Cells: {Y.shape[1]}",
        f"- LOO features evaluated: {len(loo_cols)} "
        f"({len(latent_cols)} latent + {len(noise_cols)} noise)",
        "- Retrain presets: `HISTGB_FULL` (max_iter=400, max_depth=8) "
        "and `HISTGB_FAST` (max_iter=100, max_depth=4 — what "
        "`feature_ablation.py` actually uses today).",
        "- Distilled presets: `HISTGB_FAST` and `HISTGB_MEDIUM` "
        "(max_iter=200, max_depth=6).",
        "- Hardware: 16-core box, `n_jobs=-1`",
        "",
        f"Full-schema baseline argmin_acc: **{baseline_metrics['argmin_acc']:.4f}** "
        f"(mean overhead {baseline_metrics['mean_overhead_pct']:.2f}%); "
        f"full teachers fit in {full_train_time:.2f}s.",
        "",
        "## Per-feature results",
        "",
        "| feature | retrain-FULL t/acc | retrain-FAST t/acc | distill-FAST t/acc | distill-MED t/acc | residual t/acc |",
        "|---|--:|--:|--:|--:|--:|",
    ]
    for r in rows:
        lines.append(
            f"| `{r['feature']}` | "
            f"{r['retrain_full_time_s']:.2f}s / {r['retrain_full_argmin_acc']:.4f} | "
            f"{r['retrain_fast_time_s']:.2f}s / {r['retrain_fast_argmin_acc']:.4f} | "
            f"{r['distilled_fast_time_s']:.2f}s / {r['distilled_fast_argmin_acc']:.4f} | "
            f"{r['distilled_med_time_s']:.2f}s / {r['distilled_med_argmin_acc']:.4f} | "
            f"{r['residual_time_s']:.2f}s / {r['residual_argmin_acc']:.4f} |"
        )

    lines += [
        "",
        "## Aggregate (vs HISTGB_FULL retrain — comparable to production teachers)",
        "",
        "| strategy | mean wall (s) | std (s) | mean acc | speedup | acc loss (pp) |",
        "|---|--:|--:|--:|--:|--:|",
        f"| retrain (HISTGB_FULL) | {re_full_t_mean:.2f} | {re_full_t_std:.2f} | {re_full_a_mean:.4f} | 1.00× | 0.00 |",
        f"| retrain (HISTGB_FAST) | {re_fast_t_mean:.2f} | {re_fast_t_std:.2f} | {re_fast_a_mean:.4f} | "
        f"{re_full_t_mean / re_fast_t_mean:.2f}× | {100*(re_full_a_mean - re_fast_a_mean):+.2f} |",
        f"| distilled (HISTGB_FAST) | {di_fast_t_mean:.2f} | {di_fast_t_std:.2f} | {di_fast_a_mean:.4f} | "
        f"{di_fast_su_full:.2f}× | {di_fast_loss_full:+.2f} |",
        f"| distilled (MEDIUM) | {di_med_t_mean:.2f} | {di_med_t_std:.2f} | {di_med_a_mean:.4f} | "
        f"{di_med_su_full:.2f}× | {di_med_loss_full:+.2f} |",
        f"| residual (HISTGB_FAST) | {rs_t_mean:.2f} | {rs_t_std:.2f} | {rs_a_mean:.4f} | "
        f"{rs_su_full:.2f}× | {rs_loss_full:+.2f} |",
        "",
        "## Aggregate (vs HISTGB_FAST retrain — apples-to-apples for feature_ablation.py)",
        "",
        "`feature_ablation.py` already uses max_iter=100, max_depth=4 for its retrain "
        "LOO, so this is the realistic comparison for any wiring change.",
        "",
        "| strategy | speedup | acc loss vs FAST retrain (pp) |",
        "|---|--:|--:|",
        f"| distilled (HISTGB_FAST) | {di_fast_su_fast:.2f}× | {di_fast_loss_fast:+.2f} |",
        f"| distilled (MEDIUM) | {di_med_su_fast:.2f}× | {di_med_loss_fast:+.2f} |",
        f"| residual (HISTGB_FAST) | {rs_su_fast:.2f}× | {rs_loss_fast:+.2f} |",
        "",
        "## Verdict",
        "",
    ]

    PASS_SPEED = 3.0
    PASS_ACC_LOSS = 1.0  # pp

    def verdict(name: str, speedup: float, loss: float) -> str:
        if speedup >= PASS_SPEED and loss <= PASS_ACC_LOSS:
            return f"**SHIP** ({name}): {speedup:.2f}× speedup, {loss:+.2f}pp acc loss — meets >={PASS_SPEED:.0f}x / <={PASS_ACC_LOSS:.1f}pp target."
        if speedup >= PASS_SPEED and loss > PASS_ACC_LOSS:
            return f"HOLD ({name}): {speedup:.2f}× speedup but {loss:+.2f}pp acc loss > {PASS_ACC_LOSS:.1f}pp budget."
        if speedup < PASS_SPEED and loss <= PASS_ACC_LOSS:
            return f"HOLD ({name}): acc OK ({loss:+.2f}pp) but only {speedup:.2f}× speedup < {PASS_SPEED:.0f}× target."
        return f"DROP ({name}): both speed ({speedup:.2f}×) and acc ({loss:+.2f}pp) miss targets."

    lines.append("Compared to HISTGB_FULL retrain (the production target):")
    lines.append("")
    lines.append("- " + verdict("distilled HISTGB_FAST", di_fast_su_full, di_fast_loss_full))
    lines.append("- " + verdict("distilled MEDIUM", di_med_su_full, di_med_loss_full))
    lines.append("- " + verdict("residual HISTGB_FAST", rs_su_full, rs_loss_full))
    lines.append("")
    lines.append("Compared to HISTGB_FAST retrain (what feature_ablation.py uses today):")
    lines.append("")
    lines.append("- " + verdict("distilled HISTGB_FAST", di_fast_su_fast, di_fast_loss_fast))
    lines.append("- " + verdict("distilled MEDIUM", di_med_su_fast, di_med_loss_fast))
    lines.append("- " + verdict("residual HISTGB_FAST", rs_su_fast, rs_loss_fast))

    lines += [
        "",
        "## Reproduction",
        "",
        "```",
        "python3 benchmarks/loo_warmstart_bench_2026-05-17.py",
        "```",
        "",
        f"Raw per-feature data: `loo_warmstart_bench_2026-05-17.tsv`.",
        "",
    ]

    OUT_MD.write_text("\n".join(lines))
    sys.stderr.write(f"[bench] wrote {OUT_MD}\n")

    # Exit code reflects whether at least one warm-start strategy
    # meets the gate against the FULL retrain baseline (the harder
    # bar — feature_ablation.py uses FAST so any FULL-beating result
    # is automatically a FAST-beating result too).
    any_ok = (
        (di_fast_su_full >= PASS_SPEED and di_fast_loss_full <= PASS_ACC_LOSS)
        or (di_med_su_full >= PASS_SPEED and di_med_loss_full <= PASS_ACC_LOSS)
        or (rs_su_full >= PASS_SPEED and rs_loss_full <= PASS_ACC_LOSS)
    )
    return 0 if any_ok else 1


